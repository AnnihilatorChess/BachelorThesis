"""Benchmark the 1D Burgers DataLoader under varying settings.

Uses the synthetic dataset generated by gen_synthetic_burgers.py. Iterates
two epochs per config, measures the second epoch's steady-state throughput
(the first captures worker start-up), and prints a summary table.

Usage:
    python scripts/benchmarks/gen_synthetic_burgers.py --n_traj 2000
    python scripts/benchmarks/bench_burgers_dataloader.py [--iters_per_epoch 100]
"""

from __future__ import annotations

import argparse
import dataclasses
import json
import os
import platform
import time
from pathlib import Path
from typing import Optional

import torch
from torch.utils.data import DataLoader

from the_well.data.datasets import WellDataset

DATA_ROOT = Path("datasets/pdebench_1d_burgers")
DATASET_NAME = "pdebench_1d_burgers"
# WellDataset whitelists dataset names. Use the real name; the directory
# happens to hold synthetic data of the same format.


@dataclasses.dataclass
class Config:
    label: str
    batch_size: int
    num_workers: int
    persistent_workers: bool
    prefetch_factor: Optional[int]
    pin_memory: bool


CONFIGS: list[Config] = [
    # Baseline (what the real runs use: 8 workers, pin_memory, no persistent, default prefetch)
    Config("baseline_w8",            1024, 8,  False, 2, True),
    # num_workers sweep
    Config("w0",                     1024, 0,  False, None, True),
    Config("w2",                     1024, 2,  False, 2, True),
    Config("w4",                     1024, 4,  False, 2, True),
    Config("w16",                    1024, 16, False, 2, True),
    # persistent_workers
    Config("w8_persistent",          1024, 8,  True,  2, True),
    # prefetch factor sweep
    Config("w8_prefetch4",           1024, 8,  False, 4, True),
    Config("w8_prefetch8",           1024, 8,  False, 8, True),
    Config("w8_persistent_prefetch4", 1024, 8, True,  4, True),
    Config("w8_persistent_prefetch8", 1024, 8, True,  8, True),
    # pin memory
    Config("w8_nopin",               1024, 8,  False, 2, False),
    # batch size sweep (keep prefetch small to be fair; persistent=False default)
    Config("bs256_w8",                256, 8,  False, 2, True),
    Config("bs512_w8",                512, 8,  False, 2, True),
    Config("bs2048_w8",              2048, 8,  False, 2, True),
    # "best guess" combo
    Config("w8_persistent_prefetch4_nopin", 1024, 8, True, 4, False),
    # Top-end combos
    Config("w12_persistent_prefetch4",      1024, 12, True, 4, True),
    Config("w16_persistent_prefetch4",      1024, 16, True, 4, True),
    Config("w16_persistent_prefetch8",      1024, 16, True, 8, True),
    Config("w16_persistent_prefetch4_bs512", 512, 16, True, 4, True),
]


def build_dataset() -> WellDataset:
    return WellDataset(
        well_base_path=str(DATA_ROOT.parent.resolve()),
        well_dataset_name=DATASET_NAME,
        well_split_name="train",
        n_steps_input=4,
        n_steps_output=1,
        use_normalization=True,
    )


def build_loader(ds: WellDataset, cfg: Config) -> DataLoader:
    kwargs = dict(
        batch_size=cfg.batch_size,
        num_workers=cfg.num_workers,
        pin_memory=cfg.pin_memory,
        shuffle=True,
        drop_last=True,
    )
    if cfg.num_workers > 0:
        kwargs["persistent_workers"] = cfg.persistent_workers
        if cfg.prefetch_factor is not None:
            kwargs["prefetch_factor"] = cfg.prefetch_factor
    return DataLoader(ds, **kwargs)


def run_config(ds: WellDataset, cfg: Config, iters_per_epoch: int) -> dict:
    """Run two epochs of iters_per_epoch batches each, return timings."""
    loader = build_loader(ds, cfg)
    results = {}
    for epoch in (1, 2):
        torch.manual_seed(1000 + epoch)
        it = iter(loader)
        # Warm up: first batch pays worker spawn
        t_first = time.perf_counter()
        batch = next(it)
        t_after_first = time.perf_counter()
        first_batch_s = t_after_first - t_first

        # Steady-state timing
        t0 = time.perf_counter()
        n = 1  # already pulled one
        for _ in range(iters_per_epoch - 1):
            try:
                batch = next(it)
            except StopIteration:
                break
            n += 1
        t1 = time.perf_counter()
        steady_s = t1 - t_after_first
        per_iter = steady_s / max(n - 1, 1)
        samples_per_s = (cfg.batch_size * (n - 1)) / max(steady_s, 1e-9)
        results[f"epoch{epoch}"] = {
            "first_batch_s": first_batch_s,
            "steady_s": steady_s,
            "n_batches": n,
            "per_iter_s": per_iter,
            "samples_per_s": samples_per_s,
        }
        del it
    # Cleanup workers
    del loader
    return results


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--iters_per_epoch", type=int, default=50,
                        help="batches to pull per epoch (each config runs 2 epochs)")
    parser.add_argument("--only", type=str, default=None,
                        help="comma-separated subset of config labels to run")
    parser.add_argument("--out_json", type=Path, default=Path("scripts/benchmarks/bench_results.json"))
    args = parser.parse_args()

    assert DATA_ROOT.exists(), f"Run gen_synthetic_burgers.py first (missing {DATA_ROOT})"

    print(f"Python: {platform.python_version()}, torch: {torch.__version__}")
    print(f"Platform: {platform.system()} {platform.release()}")
    print(f"CPU count: {os.cpu_count()}")
    print(f"Data path: {DATA_ROOT.resolve()}")
    train_file = DATA_ROOT / "data" / "train" / f"{DATASET_NAME}.hdf5"
    print(f"Train file size: {train_file.stat().st_size / 1e6:.1f} MB")
    print(f"Iters per epoch: {args.iters_per_epoch}")
    print()

    ds = build_dataset()
    print(f"Dataset length: {len(ds)} windows")
    print()

    selected = (
        [c for c in CONFIGS if c.label in set(args.only.split(","))]
        if args.only else CONFIGS
    )

    all_results = {}
    header = f"{'label':38s} {'bs':>5s} {'w':>3s} {'pers':>5s} {'pref':>5s} {'pin':>4s} | {'E1 1st':>7s} {'E1 iter':>8s} {'E1 samp/s':>10s} | {'E2 1st':>7s} {'E2 iter':>8s} {'E2 samp/s':>10s}"
    print(header)
    print("-" * len(header))
    for cfg in selected:
        try:
            r = run_config(ds, cfg, args.iters_per_epoch)
        except Exception as e:
            print(f"{cfg.label}: ERROR {e}")
            continue
        e1, e2 = r["epoch1"], r["epoch2"]
        row = (
            f"{cfg.label:38s} {cfg.batch_size:>5d} {cfg.num_workers:>3d} "
            f"{str(cfg.persistent_workers):>5s} {str(cfg.prefetch_factor):>5s} "
            f"{str(cfg.pin_memory):>4s} | "
            f"{e1['first_batch_s']:>7.2f} {e1['per_iter_s']:>8.3f} {e1['samples_per_s']:>10.0f} | "
            f"{e2['first_batch_s']:>7.2f} {e2['per_iter_s']:>8.3f} {e2['samples_per_s']:>10.0f}"
        )
        print(row)
        all_results[cfg.label] = {
            "config": dataclasses.asdict(cfg),
            "results": r,
        }

    args.out_json.parent.mkdir(parents=True, exist_ok=True)
    args.out_json.write_text(json.dumps(all_results, indent=2))
    print(f"\nsaved: {args.out_json}")


if __name__ == "__main__":
    main()
